CREATE TABLE IF NOT EXISTS public.precios_servicios (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  servicio text NOT NULL,
  tipo text NOT NULL CHECK (tipo IN ('CC', 'CE')),
  capacidad_min_mw numeric NOT NULL,
  capacidad_max_mw numeric,
  precio numeric NOT NULL,
  factor_inflacion_anual numeric DEFAULT 0.05,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_precios_servicios_lookup
  ON public.precios_servicios (tipo, servicio, capacidad_min_mw);

ALTER TABLE public.precios_servicios ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS precios_servicios_select ON public.precios_servicios;
CREATE POLICY precios_servicios_select ON public.precios_servicios
  FOR SELECT TO authenticated USING (true);

DROP POLICY IF EXISTS precios_servicios_insert ON public.precios_servicios;
CREATE POLICY precios_servicios_insert ON public.precios_servicios
  FOR INSERT TO authenticated WITH CHECK (public.app_has_any_rol('direccion','admin'));

DROP POLICY IF EXISTS precios_servicios_update ON public.precios_servicios;
CREATE POLICY precios_servicios_update ON public.precios_servicios
  FOR UPDATE TO authenticated
  USING (public.app_has_any_rol('direccion','admin'))
  WITH CHECK (public.app_has_any_rol('direccion','admin'));

DROP POLICY IF EXISTS precios_servicios_delete ON public.precios_servicios;
CREATE POLICY precios_servicios_delete ON public.precios_servicios
  FOR DELETE TO authenticated
  USING (public.app_has_any_rol('direccion','admin'));

